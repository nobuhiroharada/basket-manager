import UIKit

extension UIColor {
    
    class var limegreen: UIColor {
        return self.init(red: 173/255.0, green: 255/255.0, blue: 47/255.0, alpha: 1.0)
    }
    
    class var gold: UIColor {
        return self.init(red: 255/255.0, green: 215/255.0, blue: 0/255.0, alpha: 1.0)
    }
}
